// Helper functions
export function getStaticRivers(): StaticRiver[] {
  return staticRivers
}

export function getStaticRiver(slug: string): StaticRiver | undefined {
  return staticRivers.find(r => r.slug === slug)
}

export function getStaticRoutes(): StaticRoute[] {
  return staticRoutes
}

export function getStaticRoutesByRiver(riverSlug: string): StaticRoute[] {
  return staticRoutes.filter(r => r.riverSlug === riverSlug)
}

export function getStaticRoute(slug: string): StaticRoute | undefined {
  return staticRoutes.find(r => r.slug === slug)
}

export function getBranch(slug: string): Branch | undefined {
  return branches.find(b => b.slug === slug)
}

export function getBranches(): Branch[] {
  return branches
}

export function getBranchForRiver(riverSlug: string): Branch | undefined {
  return branches.find(b => b.rivers.includes(riverSlug))
}
